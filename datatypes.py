# Introduction to Python Data Types

# Integer (whole numbers)
x = 5
print(f"Integer: {x}")

# Float (decimal numbers)
y = 3.14
print(f"Float: {y}")

# String (text)
name = "Alice"
print(f"String: {name}")

# Boolean (True or False)
is_active = True
print(f"Boolean: {is_active}")

# List (ordered collection of items)
colors = ["red", "blue", "green"]
print(f"List: {colors}")
